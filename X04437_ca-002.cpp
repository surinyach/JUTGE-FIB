#include <iostream>
#include <math.h>
using namespace std;

double dist_or (double x, double y) {
    
    return sqrt((x*x) + (y*y)); 
    
}

int main () {

    double abs = 0; 
    double ord = 0; 
    
    cin >> abs; 
    cin >> ord; 
    
    cout << dist_or(abs, ord) << endl; 

}
    
        
        
         
                    
            
            
        
    
        
        
            
            
            
            
        
        
        
        
        
        
        
        
     

    
             
     
                 
         
